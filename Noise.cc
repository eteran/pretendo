
#include "Noise.h"

namespace {

// NTSC period table
const uint16_t frequency_table[16] = {
	4, 8, 16, 32, 64, 96, 128, 160, 202, 254, 380, 508, 762, 1016, 2034, 4068
};

}

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
Noise::Noise() : enabled_(false), output_(0) {

}

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
Noise::~Noise() {

}

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
void Noise::enable() {
	enabled_ = true;
}

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
void Noise::disable() {
	enabled_ = false;
	length_counter_.clear();
}

//------------------------------------------------------------------------------
// Name: write_reg0
//------------------------------------------------------------------------------
void Noise::write_reg0(uint8_t value) {

	if(value & 0x20) {
		length_counter_.halt();
	} else {
		length_counter_.resume();
	}
	
	envelope_.set_constant(value & 0x10);
	envelope_.set_divider(value & 0x0f);
}

//------------------------------------------------------------------------------
// Name: write_reg2
//------------------------------------------------------------------------------
void Noise::write_reg2(uint8_t value) {
	
	lfsr_.set_mode(value & 0x80);
	timer_.set_frequency(frequency_table[value & 0x0f]);
}

//------------------------------------------------------------------------------
// Name: write_reg3
//------------------------------------------------------------------------------
void Noise::write_reg3(uint8_t value) {

	if(enabled_) {
		length_counter_.load((value >> 3) & 0x1f);
	}
	
	envelope_.start();
}

//------------------------------------------------------------------------------
// Name: enabled
//------------------------------------------------------------------------------
bool Noise::enabled() const {
	return enabled_;
}

//------------------------------------------------------------------------------
// Name: length_counter
//------------------------------------------------------------------------------
LengthCounter &Noise::length_counter() {
	return length_counter_;
}

//------------------------------------------------------------------------------
// Name: tick
//------------------------------------------------------------------------------
void Noise::tick() {
	if(timer_.tick() && enabled()) {
		lfsr_.clock();
	}
}

//------------------------------------------------------------------------------
// Name: dac
//------------------------------------------------------------------------------
uint8_t Noise::output() const {
	if(length_counter_.value() == 0 || ((lfsr_.value() & 1) == 0)) {
		return 0;
	} else {
		return envelope_.volume();
	}
}

//------------------------------------------------------------------------------
// Name: envelope
//------------------------------------------------------------------------------
Envelope &Noise::envelope() {
	return envelope_;
}
