
#include "Mapper034.h"

#include "BNROM.h"
#include "NINA-001.h"

SETUP_STATIC_INES_MAPPER_REGISTRAR(34);

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
Mapper34::Mapper34() {

	// for now, this is how we tell the difference, iNES2.0 sub mappers
	// or UNIF would be preferable
	if(nes::cart.has_chr_rom()) {
		mapper_ = new NINA_001;
	} else {
		mapper_ = new BNROM;
	}
}

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
Mapper34::~Mapper34() {
	delete mapper_;
}

//------------------------------------------------------------------------------
// Name:
//------------------------------------------------------------------------------
std::string Mapper34::name() const {
	return mapper_->name();
}

