void Mapper118::write_a(uint16_t address, uint8_t value) {
	switch(address & 0x0001) {
	case 0x0000:
		// ignore these mirror writes, it is handled differently
		break;
	case 0x0001:
		mapper_->write_a(address, value);
		break;
	}
}

