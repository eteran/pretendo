
#include "Timer.h"

//------------------------------------------------------------------------------
// Name: Timer
//------------------------------------------------------------------------------
Timer::Timer() : timer_(0xffff), frequency_(0xffff) {

}

//------------------------------------------------------------------------------
// Name: tick
//------------------------------------------------------------------------------
bool Timer::tick() {
	if(--timer_ == 0) {
		timer_ = frequency_;
		return true;
	}

	return false;
}

//------------------------------------------------------------------------------
// Name: set_frequency
//------------------------------------------------------------------------------
void Timer::set_frequency(uint16_t frequency) {
	frequency_ = frequency;
}

//------------------------------------------------------------------------------
// Name: reset
//------------------------------------------------------------------------------
void Timer::reset() {
	timer_ = frequency_;
}

//------------------------------------------------------------------------------
// Name: frequency
//------------------------------------------------------------------------------
uint16_t Timer::frequency() const {
	return frequency_;
}

//------------------------------------------------------------------------------
// Name: value
//------------------------------------------------------------------------------
uint16_t Timer::value() const {
	return timer_;
}
